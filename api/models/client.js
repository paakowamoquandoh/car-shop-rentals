import mongoose from "mongoose";
const ClientSchema = new mongoose.Schema(
  {
    clientname: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    idType: {
      type: String,
      required: true,
    },
    idNumber: {
        type: String,
        required: true,
      },
    img: {
      type: String,
    },
    city: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

export default mongoose.model("Client", ClientSchema);